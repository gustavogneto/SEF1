public void BuscarUsuario() {
        try {
            Connection con = ConnectionFactory.getConnection("mysql");
            
            String sql = "select CONCAT(SUBSTRING(usuario.cpf,1,3),'.',\n" +
"              SUBSTRING(usuario.cpf,4,3),'.',\n" +
"              SUBSTRING(usuario.cpf,7,3),'-',\n" +
"              SUBSTRING(usuario.cpf,10,2)) AS cpf, nome, sistema from usuario,  user_sis, sistema  where usuario.cpf = user_sis.cpf and sistema.id = user_sis.id";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) recusuarios.getModel();
            model.setNumRows(0);
            while (rs.next()) {
                String[] linha = {rs.getString("cpf"), rs.getString("nome").toUpperCase(), rs.getString("sistema").toUpperCase()};
                model.addRow(linha);
            }

            stmt.close();
            con.close();

        } catch (SQLException ex) {
            System.out.print(ex);
        } catch (ClassNotFoundException ex) {
             System.out.print(ex);
        }
    
    }


